    private static bool Matches(string name, string[] keywords)
    {
        var n = name.ToLowerInvariant().Trim();
        if (string.IsNullOrEmpty(n)) return false;
        return keywords.Any(kw => n.Contains(kw));
    }

    /// <summary>Проверить пару препаратов на взаимодействия.</summary>
    public static List<InteractionHit> CheckPair(string nameA, string nameB)
    {
        var hits = new List<InteractionHit>();
        foreach (var rule in Rules)
        {
            if ((Matches(nameA, rule.KeywordsA) && Matches(nameB, rule.KeywordsB)) ||
                (Matches(nameA, rule.KeywordsB) && Matches(nameB, rule.KeywordsA)))
            {
                hits.Add(new InteractionHit(nameA, nameB, rule.Severity, rule.Note));
            }
        }
        return hits;
    }

    /// <summary>Проверить все пары из секционного списка препаратов.</summary>
    public static List<InteractionHit> CheckAll(IEnumerable<MedicationSection> sections)
    {
        var names = sections
            .SelectMany(s => s.Items)
            .Select(m => m.Name)
            .Where(n => !string.IsNullOrEmpty(n))
            .Distinct(StringComparer.OrdinalIgnoreCase)
            .ToList();

        var results = new List<InteractionHit>();
        var seen = new HashSet<string>();

        for (int i = 0; i < names.Count; i++)
        {
            for (int j = i + 1; j < names.Count; j++)
            {
                var key = string.Join("|", new[] { names[i].ToLower(), names[j].ToLower() }.OrderBy(x => x));
                if (!seen.Add(key)) continue;
                results.AddRange(CheckPair(names[i], names[j]));
            }
        }

        // Сортировка: High → Medium → Info
        results.Sort((a, b) => a.Severity.CompareTo(b.Severity));
        return results;
    }

    /// <summary>Проверить один новый препарат против всего списка (при добавлении).</summary>
    public static List<InteractionHit> CheckNewAgainstExisting(string newName, IEnumerable<MedicationSection> sections)
    {
        var existing = sections
            .SelectMany(s => s.Items)
            .Select(m => m.Name)
            .Where(n => !string.IsNullOrEmpty(n) &&
                        !string.Equals(n, newName, StringComparison.OrdinalIgnoreCase))
            .Distinct(StringComparer.OrdinalIgnoreCase);

        var results = new List<InteractionHit>();
        foreach (var name in existing)
            results.AddRange(CheckPair(newName, name));

        results.Sort((a, b) => a.Severity.CompareTo(b.Severity));
        return results;
    }

